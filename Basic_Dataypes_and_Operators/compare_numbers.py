# This script compares two numbers
num1 = float(input("Enter the first number: "))  # Get the first number from the user
num2 = float(input("Enter the second number: ")) # Get the second number from the user

# Compare the numbers using conditional statements
if num1 > num2:
    print("The first number is greater than the second number.")
elif num1 < num2:
    print("The first number is less than the second number.")
else:
    print("The first number is equal to the second number.")