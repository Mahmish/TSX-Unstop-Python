# Addition (+): Adds two operands.
# >>> 5 + 3
# 8
# >>> 2.5 + 7
# 9.5

# Subtraction (-): Subtracts the second operand from the first.
# >>> 10 - 4
# 6
# >>> 15.8 - 3.2
# 12.6

# Multiplication (*): Multiplies two operands.
# >>> 6 * 7
# 42
# >>> 2 * 3.14
# 6.28

# Division (/): Divides the first operand by the second.  It always returns a float.
# >>> 12 / 4
# 3.0
# >>> 10 / 3
# 3.3333333333333335

# Floor Division (//): Divides the first operand by the second, returning the integer part of the quotient (removes the decimal part).
# >>> 10 // 3
# 3
# >>> 15 // 4
# 3
# >>> -10 // 3
# -4

# Modulo (%): Returns the remainder of the division of the first operand by the second.
# >>> 10 % 3
# 1
# >>> 16 % 5
# 1

# Exponentiation (**): Raises the first operand to the power of the second.
# >>> 2 ** 3
# 8
# >>> 5 ** 2
# 25


