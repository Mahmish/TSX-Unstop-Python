# or: Returns True if at least one of the operands is True, returns False only if both are False.
# >>> True or True
# True
# >>> True or False
# True
# >>> False or True
# True
# >>> False or False
# False
# >>> (5 > 3) or (10 > 20)
# True
# >>> (5 < 3) or (10 > 20)
# False

# not:Returns True if the operand is False, and False if the operand is True.
# >>> not True
# False
# >>> not False
# True
# >>> not (5 > 3)
# False
# >>> not (5 < 3)
